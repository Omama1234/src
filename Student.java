// package pack2;
public class Student {
    private String name; // by default value will be null
    private int rollno; // by defualt value will be 0
    public void setName(String name){
        this.name = name;
    }
    public void setRollno(int rollno){
        this.rollno = rollno;
    }
    public String getName(){
        return this.name;
    }
    public int getRollno(){
        return this.rollno;
    }
}
